    <p>Ein kurzer Überblick über die wichtigsten Veränderungen in Version 1.2:</p>
    <ul>
      <li>
        <b>Automatisches Speichern:</b>  Änderungen im Editor werden nun automatisch gespeichert, sowohl in regelmäßigen Abständen als auch beim Schließen oder Ausloggen.  Die &bdquo;Datei speichern&ldquo;-Schaltfläche wurde entfernt.  Bei kritischen Problemen beim Speichervorgang werden Sie rechtzeitig benachrichtigt.
      </li>
      <li>
        <b>Rückgängig/Wiederherstellen:</b>  Änderungen im Editor können jetzt rückgängig gemacht bzw. wiederhergestellt werden.
      </li>
      <li>
        <b>Suchfunktion:</b>  Der aktuell geöffnete Text kann gezielt nach bestimmten Tokens durchsucht werden.  Starten Sie die Suche über die Schaltfläche &bdquo;Suchen...&ldquo;; die Suchergebnisse werden in einem separaten Tab neben dem Editor-Tab angezeigt.
      </li>
      <li>
        <b>Automatische Annotation:</b>  CorA kann auf externe Software zugreifen, um bestimmte Annotationsebenen automatisch zu füllen.  Das Einrichten dieser Option kann nur ein Administrator vornehmen.
      </li>
      <li>
        <b>Export im Spaltenformat:</b>  Dokumente können als mehrspaltige Textdatei exportiert werden, wobei die Annotationsebenen, die exportiert werden sollen, frei ausgewählt werden können.
      </li>
      <li>
        Zahlreiche kleinere Verbesserungen, sowie umfangreiche interne Verbesserungen des Programmcodes.
      </li>
    </ul>
    <p>Mehr Informationen über die neuen Funktionen können Sie dem <a href="doc/cora-guide.pdf" download="cora-guide.pdf">Benutzerhandbuch</a> entnehmen!</p>
